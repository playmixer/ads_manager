import re


def ip_address(ip):
    aa = re.match(
        r"^(25[0-5]|2[0-4][0-9]|[01]?[1-9][0-9]?)\." +
        r"(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\." +
        r"(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\." +
        r"(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$",
        ip)
    if aa:
        return aa.group()
    return False
